def require_role(allowed_roles: list[UserRole]):
    """Dependency to require specific roles."""
    async def role_checker(current_user: User = Depends(get_current_user)) -> User:
        if current_user.role not in allowed_roles:
            raise HTTPException(
                status_code=status.HTTP_403_FORBIDDEN,
                detail="Not enough permissions"
            )
        return current_user
    return role_checker


# Role-based dependencies
require_admin = require_role([UserRole.ADMINISTRATOR])
require_manager = require_role([UserRole.ADMINISTRATOR, UserRole.MANAGER])
require_viewer = require_role([UserRole.ADMINISTRATOR, UserRole.MANAGER, UserRole.VIEWER])
